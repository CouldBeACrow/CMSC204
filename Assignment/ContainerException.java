/**
 * 
 * @author jake
 * Exception for container
 */
public class ContainerException extends Exception {

	/**
	 * Default constructor
	 */
	public ContainerException() {
		super();
	}
	
	/**
	 * Constructor
	 * @param message
	 */
	public ContainerException(String message) {
		super(message);
	}
	
}
